import java.util.*;

public class datatypes {
    public static void main(String args[]){
        int a=10;
        double b=3678.46d;
        short c=13;
        boolean v=true;
        float h=5.5f;
        byte m=67;
        String k="rain";
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(v);
        System.out.println(h);
        System.out.println(k);
        System.out.println(m);
    }

}
